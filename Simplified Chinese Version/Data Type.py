'''
以下为数字（number）类型
'''
integer_1 = 1  # 将整型数据1赋值给名为integer_a的变量
float_1 = 3.1415  # 将浮点型数据3.1415赋值给名为float_b的变量
complex_1 = 4+5j  # 将复数型数据4+5j赋值给名为complex_c的变量

'''
以下为布尔（boolean/bool）类型
'''
bool_1 = True
bool_2 = False
bool_3 = 1
bool_4 = 0

'''
以下为字符串（string）类型
'''
string_1 = '76134.234'  # 将字符串型数据76134.234赋值给名为string_d的变量
string_2 = 'string datatype'

'''
以下为元组（tuple）类型
'''
tuple_1 = (1, 2, 3)

'''
以下为列表（list）类型
'''
list_1 = [4, 5, 6]

'''
以下为集合（set）类型
'''
set_1 = {7, 8, 9}
'''
以下为字典（dictionary）类型
'''
dictionary_1 = {'a':1, 'b':2}

'''
使用print()函数打印输出变量以及其对应的数据类型
'''
print(integer_1, type(integer_1))
print(float_1, type(float_1))
print(complex_1, type(complex_1))

print(string_1, type(string_1))
